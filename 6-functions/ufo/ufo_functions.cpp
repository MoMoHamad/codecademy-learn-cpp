void end_game(std::string answer, std::string codeword) {
    if (answer == codeword) {
        std::cout << "\n" << "Hooray! You saved the person and earned a medal of honor!" << "\n";
    }
    else {
        std::cout << "\n" << "Oh no! The UFO just flew away with another person!" << "\n";
    }
}

void display_status(std::vector<char> incorrect, std::string answer) {
    std::cout << "Incorrect Guesses:" << "\n";
    for (char i : incorrect) {
        std::cout << i << ' ';
    }

    std::cout << "Codeword:" << "\n";
    for (char j : answer) {
        std::cout << j << ' ';
    }

    std::cout << "\n";
}
